package com.rtnr.data;

public class Coordinate {
	
	private double lat;
	private double lng;
	
	public double getLat() {
		return lat;
	}
	public void setLat(double latitude) {
		this.lat = latitude;
	}
	public double getLng() {
		return lng;
	}
	public void setLng(double longitude) {
		this.lng = longitude;
	}
	
}
